    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        int indexRow = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        String messege = "nama Makanan : "+model.getValueAt(indexRow,0)+"\nJenis Makanan : "+model.getValueAt(indexRow,1)+"\nRating :"+model.getValueAt(indexRow,2)+"\nKalori : "+model.getValueAt(indexRow,3);
        JOptionPane.showMessageDialog(this, messege);
    }//GEN-LAST:event_btnDetailActionPerformed

