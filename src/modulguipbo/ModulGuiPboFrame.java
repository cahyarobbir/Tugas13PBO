    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        String rating ="";
        if(ratingSatu.isSelected()){
            rating = "1";
        }
        else if(ratingDua.isSelected()){
            rating = "2";
        }
        else if(ratingTiga.isSelected()){
            rating = "3";
        }
        String data [] = {etNamaMakanan.getText(),etJenisMakanan.getText(),rating,kalori.getText()};
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.addRow(data);
    }//GEN-LAST:event_btnTambahActionPerformed

